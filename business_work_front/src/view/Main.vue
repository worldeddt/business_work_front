<script>
import ProjectRegister from "./modal/ProjectRegister";

export default {
  data: () => ({
    drawer : false,
    storeData : [],
    loading : false,
    optionBtnEn : {"삭제":"delete", "수정" : "update"},
    optionBtn : ["삭제", "수정"]
  }),
  props: {
    source: String
  },
  methods : {
    routerProject(projectIndex) {
      console.log(projectIndex);
      if(this.$route.path !== `/project/${projectIndex}`) {
        this.$router.push({path:`/project/${projectIndex}`})
      }
    },
    showProjectRegister() {
      const modal = this.$modal;

      modal.show(ProjectRegister,{
        draggable : false 
      });
    },
    deleteProject(_projectIndex) {
      const store = this.$store;

      const promise = new Promise(function (resolve) {
        store.dispatch("removeProject", {"projectId" : _projectIndex});
        resolve("success");
      });

      promise.then(function(_result) {
        console.log(_result)
      });        
    },
    updateProject(_projectIndex) {
      console.log(_projectIndex);
      console.log(this.$store.getters.getProjectList)

      for (let project of this.$store.getters.getProjectList) {
        if (Number(project.index) === Number(_projectIndex)) {
          project.title

          modal.show(ProjectRegister,{
            draggable : false 
          },
          function() {
            console.log('test');
          }); 
        }
      }
    },
    closeModal() {
      const modal = this.$modal;

      modal.show(ProjectRegister,{
        draggable : false 
      });
    },
    modifyProject(_flag, _projectIndex) {
      if (!_projectIndex) alert("동작을 수행할 수 없습니다.");

      if (_flag === "delete") {
        this.deleteProject(_projectIndex)
      }

      if (_flag === "update") {
        this.updateProject(_projectIndex);
      }
    }
  },
  watch : { 
  },
  created() {
    this.$store.dispatch("delayAllProjectFetch");
  }
}

</script>
